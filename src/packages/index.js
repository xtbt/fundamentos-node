const isOdd = require('is-odd');
const isEven = require('is-even');

console.log(isOdd(1)); // Prints true because 1 is ODD
console.log(isOdd(2)); // Prints false because 2 is not ODD

console.log(isEven(1)); // Prints false because 1 is not EVEN
console.log(isEven(2)); // Prints true because 2 is EVEN
